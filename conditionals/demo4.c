#include <stdio.h>


int main()
{
	int num = 1;
	switch(num)
	{
		case 1:
			printf("The value of num is 1\n");
			break;
		case 2:
			printf("The value of num is 2\n");
			break;
		case 3:
			printf("The value of num is 3\n");
			break;
		default:
			printf("the value is invalid\n");
	}
	return (0);
}
